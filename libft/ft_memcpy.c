#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	if (dst == NULL && src == NULL)
		return (NULL);
	while (i < n)
	{
		*(unsigned char *)dst++ = *(unsigned char *)src++;
		i++;
	}
	while (n)
	{
		dst--;
		n--;
	}
	return (dst);
}
